ngay_trong_tu = int(input("Nhập vào số ngày trong tuần: "))
if ngay_trong_tu == 1:
    print("Monday")
elif ngay_trong_tu == 2:
    print("Tuesday")
elif ngay_trong_tu == 3:
    print("Wednesday")
elif ngay_trong_tu == 4:
    print("Thursday")
elif ngay_trong_tu == 5:
    print("Friday")
elif ngay_trong_tu == 6:
    print("Saturday")
elif ngay_trong_tu == 7:
    print("Sunday")
else:
    print("error, out of range")